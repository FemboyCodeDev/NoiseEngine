package noise;

public class vec2D {

    public float x;
    public float y;
    public float scale;

    public vec2D(float x, float y, float scale) {
        this.x = x;
        this.y = y;
        this.scale = scale;



    }
    public vec2D(float x, float y){
        this.x = x;
        this.y = y;
        this.scale = 1;

    }
}
